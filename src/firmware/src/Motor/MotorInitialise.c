#include <xc.h>

#include "Motor.h"

void motorInitialise(void)
{
	LATC &= 0b00110011;
	ANSELC &= 0b00110011;
	TRISC &= 0b00110011;

	LATBbits.LATB1 = 0;
	ANSELBbits.ANSB1 = 0;
	TRISBbits.TRISB1 = 0;
}

